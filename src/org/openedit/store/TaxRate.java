package org.openedit.store;

import org.openedit.money.Fraction;

public class TaxRate {

	protected Fraction fieldFraction;
	protected String fieldState;
	protected boolean fieldApplyToShipping;
	protected String fieldName;
	
	public Fraction getFraction() {
		return fieldFraction;
	}
	public void setFraction(Fraction inFraction) {
		fieldFraction = inFraction;
	}
	public String getState() {
		return fieldState;
	}
	public String getName() {
		return fieldName;
	}
	public void setName(String inName) {
		fieldName = inName;
	}
	public void setState(String inState) {
		fieldState = inState;
	}
	public boolean isApplyToShipping() {
		return fieldApplyToShipping;
	}
	public void setApplyToShipping(boolean inApplyToShipping) {
		fieldApplyToShipping = inApplyToShipping;
	}
	
	
	
}
