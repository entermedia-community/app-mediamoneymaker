package org.openedit.store.excelconvert;

/**
 * @author cburkey
 *
 */
public class Image
{
	protected String fieldImagePath;
	protected String fieldShortName;
	protected String fieldGroupId;
	
	public String getImagePath()
	{
		return fieldImagePath;
	}
	public void setImagePath(String inImagePath)
	{
		fieldImagePath = inImagePath;
	}
	public String getShortName()
	{
		return fieldShortName;
	}
	public void setShortName(String inShortName)
	{
		fieldShortName = inShortName;
	}
	public String getGroupId()
	{
		return fieldGroupId;
	}
	public void setGroupId(String inGroupId)
	{
		fieldGroupId = inGroupId;
	}
}
